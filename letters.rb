# Concatenates two strings and prints the result
puts "add these " + "strings"

# Using \t (tab) in strings to separate words with a tab space
puts "add\tthese\tstrings"

# Using \n (newline) in strings to print each word on a new line
puts "add\nthese\nstrings"

# Combining both \t (tab) and \n (newline) in a string
puts "add\tthese\nstrings"

# Prints the string "sample" four times in a row (string multiplication)
puts "sample" * 4

# Adds two integers and prints the result (integer addition)
puts 18 + 18

# Concatenates two strings and prints the result (string concatenation)
puts "12" + "12"
